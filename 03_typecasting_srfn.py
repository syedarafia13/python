# Program of Typecasting

# "TYPECASTING: Typecasting is to convert one DATATYPE into another."

'''**************** STRING TO INTEGER CONVERTOR  *******************'''
a = "1309"
a = int(a)    # Convert STRING into INTEGER
print(type(a))
print(a+5)

'''**************** INTEGER TO STRING CONVERTOR  *******************'''
b = 53
b = str(b)    # Convert INTEGER into STRING
print(type(b))

'''***************** INTEGER TO FLOAT CONVERTOR  *******************'''
c = 19
c = float(c)  # Convert INTEGER into FLOAT
print(type(c))
print(c)
print(c+5)