# Program of String Functions

language = "PYTHON LANGUAGE is a High Level Language"

print(len(language))   # Length of a string
print(language.endswith("Language"))  # String is end at language e.g. True
print(language.count("O"))  # Total Numbers of O e.g: 1
print(language.capitalize()) # Capitalize the first character
print(language.find("High"))  # Find the index of first occurance of High
print(language.replace("PYTHON", "C++")) # Replace old word with a new word
